    @parent
    <li class="breadcrumb-item active">List Agenda Rapat</li>
@endsection

@section('content')
    <div class="row">
        <div class="col-lg-12 col-md-12 col-12">
            <x-card>
                @can('Agenda Store')
                    <x-slot name="header">
                        <button onclick="addForm(`{{ route('agenda.store') }}`)" class="btn btn-sm btn-info"><i
                                class="fas fa-plus-circle"></i>
                            Tambah Data</button>
                    </x-slot>
                @endcan
                <x-table id="table" style="width: 100%">
                    <x-slot name="thead">
                        <th>No</th>
                        <th>Perihal</th>
                        <th>Tanggal</th>
                        <th>Waktu</th>
                        <th>Ruang Rapat</th>
                        <th>Aksi</th>
                    </x-slot>
                </x-table>
            </x-card>
        </div>
    </div>
    @include('agenda.form')
